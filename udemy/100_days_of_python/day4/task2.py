# Python Lists (mutable)

# We can create a simple collection of ordered items using a Python list
states_of_america = ["Delaware", "Pennsylvania", "New York", "Colorado", "Wisconsin"]
print(states_of_america[0])  # first index 0
print(states_of_america[-1])  # last index -1

states_of_america[1] = "Pencilvania"
print(states_of_america)

states_of_america.append('Hawaii')
print(states_of_america)

states_of_america.extend(['Montana','California','New Mexico'])
print(states_of_america)



