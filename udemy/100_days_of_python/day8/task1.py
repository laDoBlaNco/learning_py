# Functions with inputs. We've seen that functions allow us to package code into a named block
# which can be used repeatedly at a later point. 
def greet():
    print('hello')
    print('there')
    print('Ladoblanco')

greet()

def greet_with_name(nam:str):
    print(f'Hello {nam}')
    print(f'How do you do {nam}')

greet_with_name('Kevin')

