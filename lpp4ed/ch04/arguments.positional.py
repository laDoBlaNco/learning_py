# arguments.positional.py

# There are four different ways of passing arguments to a function:
# • Positional Arguments

# When we call a function, each positional argument is assigned to the 
# parameter in the corresponding position in the function definition:

def func(a,b,c):
    print(a,b,c)

func(1,2,3)

# this is the most common way of passing arguments to functions (in some 
# programming languages, this is the only way of passing arguments)


