# py as a calculator:
print(2+2)
print(50-5*6)
print((50-5*6)/4)

# division always returns a floating-point number
print(8/5)

# / always returns float and // returns int
# class division returns a float
print(17/3)

# floor division discards the fractional part
print(17//3)
# the % operator returns the remainder of the division
print(17%3)
#a floored quotient * divisor + remainder
print(5 * 3 + 2)
